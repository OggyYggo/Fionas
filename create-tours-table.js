// Script to create the tours table in Supabase
import { createClient } from '@supabase/supabase-js';
import { config } from 'dotenv';

config({ path: '.env.local' });

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
const supabaseAnonKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;

const supabase = createClient(supabaseUrl, supabaseAnonKey);

async function createToursTable() {
  try {
    console.log('🔍 Creating tours table...');
    
    // SQL to create the tours table
    const createTableSQL = `
      CREATE TABLE IF NOT EXISTS tours (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        title VARCHAR(255) NOT NULL,
        description TEXT NOT NULL,
        image TEXT NOT NULL,
        duration VARCHAR(50) NOT NULL,
        max_people VARCHAR(50) NOT NULL,
        price VARCHAR(50) NOT NULL,
        tag VARCHAR(100) NOT NULL,
        featured BOOLEAN DEFAULT FALSE,
        created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
        updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
      );
    `;
    
    // Execute the SQL using Supabase RPC
    const { data, error } = await supabase.rpc('exec_sql', { sql: createTableSQL });
    
    if (error) {
      console.error('❌ Error creating table:', error.message);
      
      // Try alternative approach - use raw SQL if RPC is not available
      console.log('🔍 Trying to insert sample data to test table...');
      
      const sampleTour = {
        title: 'Test Tour',
        description: 'Test description',
        image: 'https://example.com/image.jpg',
        duration: 'Full Day',
        max_people: 'Max 10',
        price: '₱3,500',
        tag: 'Test',
        featured: false
      };
      
      const { data: insertData, error: insertError } = await supabase
        .from('tours')
        .insert([sampleTour])
        .select();
      
      if (insertError) {
        console.error('❌ Table likely does not exist:', insertError.message);
        console.log('\n💡 Please run this SQL manually in your Supabase SQL Editor:');
        console.log('---');
        console.log(createTableSQL);
        console.log('---');
      } else {
        console.log('✅ Tours table exists and is working!');
        console.log('Sample tour created:', insertData[0]);
        
        // Clean up the test record
        await supabase.from('tours').delete().eq('id', insertData[0].id);
        console.log('🧹 Test record cleaned up');
      }
    } else {
      console.log('✅ Tours table created successfully!');
      
      // Create indexes for better performance
      const indexSQL = `
        CREATE INDEX IF NOT EXISTS idx_tours_featured ON tours(featured);
        CREATE INDEX IF NOT EXISTS idx_tours_tag ON tours(tag);
        CREATE INDEX IF NOT EXISTS idx_tours_created_at ON tours(created_at);
      `;
      
      console.log('🔍 Creating indexes...');
      const { data: indexData, error: indexError } = await supabase.rpc('exec_sql', { sql: indexSQL });
      
      if (indexError) {
        console.log('⚠️  Index creation failed, but table is ready:', indexError.message);
      } else {
        console.log('✅ Indexes created successfully');
      }
    }
    
  } catch (err) {
    console.error('❌ Script failed:', err.message);
  }
}

createToursTable();
